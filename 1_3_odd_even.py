
number = int (input('Input a number, please. Введіть, будь ласка, число.'))

print ('Your number is / Ваше число : {}.' .format(number))

if number % 2 == 0:
    print ('The number {} is EVEN. Число є парним.' .format(number))
else :
    print ('The number {} is ODD. Число є непарним.' .format(number))

